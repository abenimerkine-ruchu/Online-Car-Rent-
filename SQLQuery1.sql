CREATE DATABASE ONLINE_CAR_RENTAL_SYSTEM
USE ONLINE_CAR_RENTAL_SYSTEM


      CREATE TABLE PHONE
(
    PHONE_No INT PRIMARY KEY NOT NULL
);


CREATE TABLE CUSTOMER_
( 
      CUSTOMERID INT IDENTITY(20,15) PRIMARY KEY, 
      FIRSTNAME VARCHAR(30) NOT NULL, 
      LASTNAME VARCHAR(30) NOT NULL, 
      AGE INT NOT NULL CHECK (AGE >=21),
      EMAIL VARCHAR(30),
      CUSTOMER_USERNAME VARCHAR(20),
      CUSTOMER_PASSWORD VARCHAR(20), 
      REGION VARCHAR(50) NOT NULL,
      CITY VARCHAR(50) NOT NULL,
      DRIVING_LICENCE_No VARCHAR(20) NOT NULL,
      PHONE_NO INT REFERENCES PHONE (PHONE_NO));




CREATE TABLE ADMIN
(
    ADMIN_ID VARCHAR(20) PRIMARY KEY,
    FIRSTNAME VARCHAR(30),
    LASTNAME VARCHAR(30),
    ADMIN_USERNAME VARCHAR(20),
    ADMIN_PASSWORD VARCHAR(20),
    PHONE_No INT REFERENCES PHONE(PHONE_NO),
    ADMIN_ROLE VARCHAR(20),
);


CREATE TABLE MANAGER
(
    MANAGER_ID varchar(20)  PRIMARY KEY,
    FIRSTNAME VARCHAR(30),
    LASTNAME VARCHAR(30),
    MANAGER_USERNAME VARCHAR(20),
    MANAGER_PASSWORD VARCHAR(20),
    ROLE VARCHAR(20),
    EMAIL VARCHAR(30),
    PHONE_No INT REFERENCES PHONE(PHONE_No),
    ADMIN_Id VARCHAR(20) REFERENCES ADMIN (ADMIN_Id)
   );


CREATE TABLE EMPLOYEE
     ( 
       EMPLOYEE_ID VARCHAR(20) PRIMARY KEY, 
       FIRSTNAME VARCHAR(30) NOT NULL, 
       LASTNAME VARCHAR(30) NOT NULL,
       EMPLOYEE_USERNAME VARCHAR(20),
       EMPLOYEE_PASSWORD VARCHAR(30),
       EMAIL VARCHAR(30),
       ROLE VARCHAR(20), 
        );


CREATE TABLE CAR_
(
    CAR_ID VARCHAR(20) PRIMARY KEY,
    BRAND VARCHAR(30),
    PLATE_No VARCHAR(20),
    PRICE_PER_DAY VARCHAR(20),
    CAR_MODEL VARCHAR(30),
    EMPLOYEE_ID VARCHAR(20) REFERENCES EMPLOYEE(EMPLOYEE_ID),
 
);




CREATE TABLE RESERVATION
    ( 
       RESERVATION_ID VARCHAR(20) PRIMARY KEY, 
       RETURN_DATE DATE, PRICE VARCHAR(20), 
       TOTALCOST VARCHAR(20), 
       CUSTOMER_ID INT IDENTITY(20,15) REFERENCES CUSTOMER_ (CUSTOMERID) NOT NULL,
       MANAGER_ID VARCHAR(20) REFERENCES MANAGER(MANAGER_ID) NOT NULL, 
       CAR_ID VARCHAR(20) REFERENCES CAR_ (CAR_ID) NOT NULL, 
       ADMIN_ID VARCHAR(20) REFERENCES ADMIN (ADMIN_ID) NOT NULL
       );



CREATE TABLE PAYMENT
(
    PAYMENT_ID VARCHAR(20),
    PAYMENT_DATE DATE,
    PAYMENTMETHOD VARCHAR(50),
    RESERVATION_ID VARCHAR(20) REFERENCES RESERVATION(RESERVATION_ID),
    PRIMARY KEY(PAYMENT_ID)
);

CREATE TABLE REPORT
(
    REPORT_ID  VARCHAR(20) PRIMARY KEY,
    REPORT_TYPE VARCHAR(50),
    GENERATE_DATE DATE,
    MANAGER_ID VARCHAR(20) REFERENCES MANAGER (MANAGER_ID) NOT NULL,
    EMPLOYEE_ID VARCHAR(20) REFERENCES EMPLOYEE(EMPLOYEE_ID),
);



